package variables;

public class Concatination {

	public static void main(String[] args) {
		System.out.println("Program start");
		byte num1=10;
		short num2=20;
		int num3=30;
		long num4=40;
		float num5=50.50f;
		double num6=60.50d;
		char character='A';
		boolean cond=false;
		System.out.println("byte is "+num1);
		System.out.println("short is "+num2);
		System.out.println("int is "+num3);
		System.out.println("long is "+num4);
		System.out.println("float is "+num5);
		System.out.println("double is "+num6);
		System.out.println("char is "+character);
		System.out.println("cond is "+cond);
		System.out.println("cond is "+cond+num1+num2);
		System.out.println("Program End");
	

	}

}
